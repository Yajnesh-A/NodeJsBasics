const os = require('os');

//To get the platform
console.log(os.platform());

//To know CPU architecture
console.log(os.arch());

//To know CPU core
console.log(os.cpus());

//To know free memory
console.log(os.freemem());

//To know total memory
console.log(os.totalmem());

//To get home directory
console.log(os.homedir());

//To get home directory
console.log(os.homedir());

//To check the system's uptime
console.log(os.uptime());